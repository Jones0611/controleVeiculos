    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DAO dao = new DAO();
        Veiculo veiculo = new Veiculo();

        String placa = (jPlaca.getText());
        veiculo.setPlaca(placa);
        try {
            dao.Pesquisar(veiculo);
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Pesquisar.class.getName()).log(Level.SEVERE, null, ex);
        }
        jMarca.setText(veiculo.getMarca());
        jModelo.setText(veiculo.getModelo());
        jVeiculo.setText(veiculo.getTipo());
        jDesc.setText(veiculo.getAnodesc());
        jStatus.setText(veiculo.getStatus());
    }//GEN-LAST:event_jButton1ActionPerformed

