    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DAO dao = new DAO();
        Veiculo veiculo = new Veiculo();

        int id = Integer.parseInt(jMarca.getText());
        String tipo = jVeiculo.getText();
        String desc = jDesc.getText();
        String status = jStatus.getText();

        veiculo.setId(id);
        veiculo.setTipo(tipo);
        veiculo.setAnodesc(desc);
        veiculo.setStatus(status);

        try {
            dao.Alterar(veiculo);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Alterar.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Alterar.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null,"Chamado alterado com sucesso!");
        jMarca.setText("");
        jVeiculo.setText("");
        jDesc.setText("");
        jStatus.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

